package com.conlect.oatos.dto.client.mail;

import java.util.List;

import org.codehaus.jackson.map.annotate.JsonSerialize;

import com.conlect.oatos.dto.client.BaseDTO;

/**
 * 
 * @author jinkerjiang
 * 
 */
@JsonSerialize(include = JsonSerialize.Inclusion.NON_NULL)
public class MailAccountsDTO implements BaseDTO {
	private static final long serialVersionUID = 1L;
	private List<MailAccountDTO> mailAccountDTOs;

	public MailAccountsDTO() {
		super();
	}

	public MailAccountsDTO(List<MailAccountDTO> mailAccountDTOs) {
		super();
		this.mailAccountDTOs = mailAccountDTOs;
	}

	public List<MailAccountDTO> getMailAccountDTOs() {
		return mailAccountDTOs;
	}

	public void setMailAccountDTOs(List<MailAccountDTO> mailAccountDTOs) {
		this.mailAccountDTOs = mailAccountDTOs;
	}

}
