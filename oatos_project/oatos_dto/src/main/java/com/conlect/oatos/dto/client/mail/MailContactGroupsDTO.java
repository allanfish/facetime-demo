package com.conlect.oatos.dto.client.mail;

import java.util.List;

import org.codehaus.jackson.map.annotate.JsonSerialize;

@JsonSerialize(include = JsonSerialize.Inclusion.NON_NULL)
public class MailContactGroupsDTO {
	private List<MailContactGroupDTO> mailContactGroups;

	public MailContactGroupsDTO() {
		super();
	}

	public MailContactGroupsDTO(List<MailContactGroupDTO> mailContactGroups) {
		super();
		this.mailContactGroups = mailContactGroups;
	}

	public List<MailContactGroupDTO> getMailContactGroups() {
		return mailContactGroups;
	}

	public void setMailContactGroups(List<MailContactGroupDTO> mailContactGroups) {
		this.mailContactGroups = mailContactGroups;
	}

}
