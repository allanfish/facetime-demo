package com.conlect.oatos.dto.status;

/**
 * file permission
 * 
 * 所有的权限设置都只针对当前文件夹的第一级子文件和子文件有效
 * 
 * @author yang
 * 
 */
public enum FilePermission {

	/**
	 * 读，查看文件的内容
	 */
	Read,
	/**
	 * 写，可以编辑文件，创建文件夹，修改文件或文件夹名字
	 */
	Write,
	/**
	 * 可以下载
	 */
	Download,
	/**
	 * 上传文件
	 */
	Upload,
	/**
	 * 删除子文件夹或者子文件
	 */
	Delete,
	/**
	 * 共享
	 */
	Share,
	/**
	 * 预览，可以在文件列表中看到文件夹下的子文件夹和文件
	 */
	List,
	/**
	 * 本地交互
	 */
	Local
}
