package com.conlect.oatos.dto.client;

import java.util.List;

/**
 * 企业网盘历史记录
 * 
 * @author yang
 * 
 */
public class ShareHistoryDTO implements BaseDTO {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 文件历史记录
	 */
	private List<ShareFileUpdateDTO> fileUpdateList;

	/**
	 * 文件夹历史记录
	 */
	private List<ShareFolderUpdateDTO> folderUpdateList;

	/**
	 * 用户id
	 */
	private long userId;

	public ShareHistoryDTO() {
	}

	public List<ShareFileUpdateDTO> getFileUpdateList() {
		return fileUpdateList;
	}

	public void setFileUpdateList(List<ShareFileUpdateDTO> fileUpdateList) {
		this.fileUpdateList = fileUpdateList;
	}

	public List<ShareFolderUpdateDTO> getFolderUpdateList() {
		return folderUpdateList;
	}

	public void setFolderUpdateList(List<ShareFolderUpdateDTO> folderUpdateList) {
		this.folderUpdateList = folderUpdateList;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

}
