package com.conlect.oatos.dto.client;

import com.conlect.oatos.dto.status.CommConstants;

/**
 * 拍照保存dto
 * 
 * @author yang
 * 
 */
public class TakePictureDTO implements BaseDTO {

	private static final long serialVersionUID = 1L;

	/**
	 * 用户id
	 */
	private long userId;

	/**
	 * 用户名
	 */
	private String userName;

	/**
	 * 企业id
	 */
	private long enterpriseId;

	/**
	 * type<br> {@link CommConstants#FILE_TYPE_ONLINEDISK}
	 * {@link CommConstants#FILE_TYPE_SHAREDISK}
	 */
	private String type;

	/**
	 * image mime type default is jpg
	 */
	private String imageType = CommConstants.FILE_FORMAT_JPG;

	/**
	 * folder id
	 */
	private Long folderId;

	/**
	 * file name
	 */
	private String name;

	/**
	 * guid
	 */
	private String guid;

	/**
	 * 文件大小，单位KB
	 */
	private long size;

	/**
	 * 文件封面
	 */
	private String thumb;

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public long getEnterpriseId() {
		return enterpriseId;
	}

	public void setEnterpriseId(long enterpriseId) {
		this.enterpriseId = enterpriseId;
	}

	public Long getFolderId() {
		return folderId;
	}

	public void setFolderId(Long folderId) {
		this.folderId = folderId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGuid() {
		return guid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	public long getSize() {
		return size;
	}

	public void setSize(long size) {
		this.size = size;
	}

	public String getImageType() {
		return imageType;
	}

	public void setImageType(String imageType) {
		this.imageType = imageType;
	}

	public String getThumb() {
		return thumb;
	}

	public void setThumb(String thumb) {
		this.thumb = thumb;
	}

}
