package com.conlect.oatos.dto.client;

import java.util.Date;

import com.conlect.oatos.dto.autobean.IConferenceMemberDTO;

/**
 * 会议参会成员
 * 
 * @author yang
 * 
 */
public class ConferenceMemberDTO implements IConferenceMemberDTO {

	private static final long serialVersionUID = 1L;

	/**
	 * 会议id
	 */
	private long conferenceId;
	/**
	 * 用户id
	 */
	private long userId;
	/**
	 * 用户名
	 */
	private String userName;
	/**
	 * 头像
	 */
	private String userIcon;
	/**
	 * 参会状态<br> {@link com.conlect.oatos.dto.status.ConferenceMemberStatus}
	 */
	private String status;

	/**
	 * 邀请人id
	 */
	private Long inviteUserId;
	/**
	 * 邀请人
	 */
	private String inviteUserName;
	/**
	 * 邀请时间
	 */
	private Date inviteTime;

	public ConferenceMemberDTO() {
	}

	@Override
	public long getConferenceId() {
		return conferenceId;
	}

	@Override
	public void setConferenceId(long conferenceId) {
		this.conferenceId = conferenceId;
	}

	@Override
	public long getUserId() {
		return userId;
	}

	@Override
	public void setUserId(long userId) {
		this.userId = userId;
	}

	@Override
	public String getUserName() {
		return userName;
	}

	@Override
	public void setUserName(String userName) {
		this.userName = userName;
	}

	@Override
	public String getUserIcon() {
		return userIcon;
	}

	@Override
	public void setUserIcon(String userIcon) {
		this.userIcon = userIcon;
	}

	@Override
	public String getStatus() {
		return status;
	}

	@Override
	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public Long getInviteUserId() {
		return inviteUserId;
	}

	@Override
	public void setInviteUserId(Long inviteUserId) {
		this.inviteUserId = inviteUserId;
	}

	@Override
	public String getInviteUserName() {
		return inviteUserName;
	}

	@Override
	public void setInviteUserName(String inviteUserName) {
		this.inviteUserName = inviteUserName;
	}

	@Override
	public Date getInviteTime() {
		return inviteTime;
	}

	@Override
	public void setInviteTime(Date inviteTime) {
		this.inviteTime = inviteTime;
	}

}
